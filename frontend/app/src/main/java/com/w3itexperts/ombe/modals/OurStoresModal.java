package com.w3itexperts.ombe.modals;

public class OurStoresModal {
    private int coffeeImage;
    private String storeName;
    private String storeTiming;
    private String storeDistance;

    public int getCoffeeImage() {
        return coffeeImage;
    }

    public void setCoffeeImage(int coffeeImage) {
        this.coffeeImage = coffeeImage;
    }

    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    public String getStoreTiming() {
        return storeTiming;
    }

    public void setStoreTiming(String storeTiming) {
        this.storeTiming = storeTiming;
    }

    public String getStoreDistance() {
        return storeDistance;
    }

    public void setStoreDistance(String storeDistance) {
        this.storeDistance = storeDistance;
    }
}
