package com.w3itexperts.ombe.modals;

public class rewardModal {
    private String rewardTitle;
    private String rewardDateTime;
    private String rewardPoints;

    public String getRewardTitle() {
        return rewardTitle;
    }

    public void setRewardTitle(String rewardTitle) {
        this.rewardTitle = rewardTitle;
    }

    public String getRewardDateTime() {
        return rewardDateTime;
    }

    public void setRewardDateTime(String rewardDateTime) {
        this.rewardDateTime = rewardDateTime;
    }

    public String getRewardPoints() {
        return rewardPoints;
    }

    public void setRewardPoints(String rewardPoints) {
        this.rewardPoints = rewardPoints;
    }
}
