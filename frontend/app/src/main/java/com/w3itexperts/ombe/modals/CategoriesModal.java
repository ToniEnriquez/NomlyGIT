package com.w3itexperts.ombe.modals;

public class CategoriesModal {
    private int coffeeImage;
    private String coffeeName;
    private String menusName;

    // Getter and Setter for coffeeImage
    public int getCoffeeImage() {
        return coffeeImage;
    }

    public void setCoffeeImage(int coffeeImage) {
        this.coffeeImage = coffeeImage;
    }

    // Getter and Setter for coffeeName
    public String getCoffeeName() {
        return coffeeName;
    }

    public void setCoffeeName(String coffeeName) {
        this.coffeeName = coffeeName;
    }

    // Getter and Setter for menusName
    public String getMenusName() {
        return menusName;
    }

    public void setMenusName(String menusName) {
        this.menusName = menusName;
    }
}
